

# Integer Variable
age = 23
print("Age:", age)

# Floating-point Variable
height = 1.60
print("Height:", height)

# String Variable
name = "Madinabonu"
print("Name:", name)

# Boolean Variable
is_student = True
print("Is Student:", is_student)

# List Variable
fruits = ["apple", "banana", "kiwi"]
print("Fruits:", fruits)

# Tuple Variable (immutable)
coordinates = (10, 20)
print("Coordinates:", coordinates)

# Dictionary Variable (key-value pairs)
person = {
    "name": "Mert",
    "age": 32,
    "city": "Istanbul"
}
print("Person Info:", person)

# Set Variable (unordered collection of unique elements)
unique_numbers = {1, 2, 3, 4, 5}
print("Unique Numbers:", unique_numbers)

# Type Checking
print("Type of age:", type(age))
print("Type of height:", type(height))
print("Type of name:", type(name))
print("Type of is_student:", type(is_student))
print("Type of fruits:", type(fruits))
print("Type of coordinates:", type(coordinates))
print("Type of person:", type(person))
print("Type of unique_numbers:", type(unique_numbers))
